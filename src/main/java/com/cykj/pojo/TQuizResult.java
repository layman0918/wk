package com.cykj.pojo;

public class TQuizResult {

  private int id;
  private int babyId;
  private int videoId;
  private int score;
  private int result;


  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }


  public int getBabyId() {
    return babyId;
  }

  public void setBabyId(int babyId) {
    this.babyId = babyId;
  }


  public int getVideoId() {
    return videoId;
  }

  public void setVideoId(int videoId) {
    this.videoId = videoId;
  }


  public int getScore() {
    return score;
  }

  public void setScore(int score) {
    this.score = score;
  }


  public int getResult() {
    return result;
  }

  public void setResult(int result) {
    this.result = result;
  }

}
