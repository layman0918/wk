package com.cykj.pojo;


public class TParam {

  private int id;
  private String name;
  private String type;
  private int value;


  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }


  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }


  public String getType() {
    return type;
  }

  public void setType(String type) {
    this.type = type;
  }


  public int getValue() {
    return value;
  }

  public void setValue(int value) {
    this.value = value;
  }

}
