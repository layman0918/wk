package com.cykj.pojo;


public class TAuthority {

  private int id;
  private int mid;
  private int role;
  private int parkId;


  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }


  public int getMid() {
    return mid;
  }

  public void setMid(int mid) {
    this.mid = mid;
  }


  public int getRole() {
    return role;
  }

  public void setRole(int role) {
    this.role = role;
  }


  public int getParkId() {
    return parkId;
  }

  public void setParkId(int parkId) {
    this.parkId = parkId;
  }

}
